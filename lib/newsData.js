export function getNewsBySlug(slug) {
  return newsPosts.find((post) => post.slug === slug);
}

export function getRelatedNews(currentSlug, count = 3) {
  const current = getNewsBySlug(currentSlug);
  if (!current) return [];
  return newsPosts
    .filter(
      (post) =>
        post.slug !== currentSlug && post.category === current.category
    )
    .concat(
      newsPosts.filter(
        (post) =>
          post.slug !== currentSlug && post.category !== current.category
      )
    )
    .slice(0, count);
}

export function getNewsByCategory(category) {
  if (category === "All") return newsPosts;
  return newsPosts.filter((post) => post.category === category);
}
